function casesByModel(model: InputModel, constraints: string[]) {
  if (model === "array") return generateArrayCases(constraints);
  if (model === "string") return generateStringCases();
  if (model === "matrix") return generateMatrixCases(constraints);
  if (model === "graph") return generateGraphCases();
  return generateNumberCases(constraints);
}

function expandToCount({
  curatedCases,
  fuzzCases,
  requiredCount,
}: {
  curatedCases: any[];
  fuzzCases: any[];
  requiredCount: number;
}) {
  const output: Array<{ category: string; input: unknown; expected: unknown; rationale: string }> = [];
  const seen = new Set<string>();

  const pushTuple = (tuple: any[], prefix: string | null) => {
    const [category, input, expected, rationale] = tuple;
    const key = stableKey(input);
    if (seen.has(key)) return;
    seen.add(key);
    output.push({ category: prefix ? `${prefix}: ${category}` : category, input, expected, rationale });
  };

  curatedCases.forEach((tuple) => pushTuple(tuple, null));
  fuzzCases.forEach((tuple) => pushTuple(tuple, "fuzz"));

  return output.slice(0, requiredCount);
}

function tupleToPlannedCase(tuple: [string, unknown, unknown, string]) {
  const [category, input, expected, rationale] = tuple;
  return { category, input, expected, rationale };
}

function buildFallbackPlannedCase({
  model,
  exampleInput,
  index,
}: {
  model: InputModel;
  exampleInput: unknown;
  index: number;
}) {
  if (exampleInput && typeof exampleInput === "object" && !Array.isArray(exampleInput)) {
    const baseInput = exampleInput as Record<string, unknown>;
    const nextInput: Record<string, unknown> = { ...baseInput };
    const keys = Object.keys(baseInput);
    const targetKey = keys[0] ?? "value";
    const currentValue = baseInput[targetKey];

    if (Array.isArray(currentValue)) {
      nextInput[targetKey] = [index, index + 1, index + 2];
    } else if (typeof currentValue === "number") {
      nextInput[targetKey] = index;
    } else if (typeof currentValue === "string") {
      nextInput[targetKey] = `fallback_${index}`;
    } else if (currentValue && typeof currentValue === "object" && Array.isArray((currentValue as any).matrix)) {
      nextInput[targetKey] = {
        ...(currentValue as any),
        matrix: [[index]],
      };
    } else if (currentValue && typeof currentValue === "object" && typeof (currentValue as any).n === "number" && Array.isArray((currentValue as any).edges)) {
      nextInput[targetKey] = {
        ...(currentValue as any),
        n: Math.max(1, index + 1),
        edges: index === 0 ? [] : [[0, 1]],
      };
    } else {
      nextInput[targetKey] = index;
    }

    return {
      category: `fallback ${targetKey}`,
      input: nextInput,
      rationale: "Last-resort unique filler to keep the generated case count exact.",
    };
  }

  if (model === "array") {
    return {
      category: "fallback array",
      input: { arr: [index, index + 1, index + 2] },
      rationale: "Last-resort unique filler to keep the generated case count exact.",
    };
  }

  if (model === "string") {
    return {
      category: "fallback string",
      input: { s: `fallback_${index}` },
      rationale: "Last-resort unique filler to keep the generated case count exact.",
    };
  }

  if (model === "matrix") {
    return {
      category: "fallback matrix",
      input: { matrix: [[index]] },
      rationale: "Last-resort unique filler to keep the generated case count exact.",
    };
  }

  if (model === "graph") {
    return {
      category: "fallback graph",
      input: { n: Math.max(1, index + 2), edges: index % 2 === 0 ? [] : [[0, 1]] },
      rationale: "Last-resort unique filler to keep the generated case count exact.",
    };
  }

  return {
    category: "fallback number",
    input: { n: index },
    rationale: "Last-resort unique filler to keep the generated case count exact.",
  };
}

function buildFuzzTuplesForModel({
  model,
  constraints,
  rng,
  targetCount,
}: {
  model: InputModel;
  constraints: string[];
  rng: () => number;
  targetCount: number;
}) {
  const tuples: any[] = [];
  const seen = new Set<string>();

  const push = (category: string, input: unknown, rationale: string) => {
    const key = stableKey(input);
    if (seen.has(key)) return;
    seen.add(key);
    tuples.push([category, input, "depends-on-spec", rationale]);
  };

  if (model === "array") {
    const arrays = generateArrayVariants(constraints, rng);
    arrays.forEach((arr) =>
      push(
        `array len=${arr.length}`,
        { arr },
        "Random/structured array variant to probe boundary and distribution behavior."
      )
    );
  } else if (model === "string") {
    const strings = generateStringVariants(rng);
    strings.forEach((s) =>
      push(`string len=${s.length}`, { s }, "Random/structured string variant to probe parsing and edge behavior.")
    );
  } else if (model === "matrix") {
    const matrices = generateMatrixVariants(constraints, rng);
    matrices.forEach((matrix) =>
      push(
        `matrix ${matrix.length}x${(matrix[0] ? matrix[0].length : 0) as number}`,
        { matrix },
        "Random/structured matrix variant to probe 2D boundary and performance behavior."
      )
    );
  } else if (model === "graph") {
    const graphs = generateGraphVariants(constraints, rng);
    graphs.forEach((g) =>
      push(
        `graph n=${g.n} m=${g.edges.length}`,
        { n: g.n, edges: g.edges },
        "Random/structured graph variant to probe traversal/cycle/connectivity behavior."
      )
    );
  } else {
    const nums = generateNumberVariants(constraints, rng);
    nums.forEach((n) =>
      push(`number n=${n}`, { n }, "Random/structured number variant to probe boundary arithmetic and validation behavior.")
    );
  }

  return tuples.slice(0, targetCount);
}

export function generateTestCases(payload: {
  challengeTitle: string;
  language: string;
  directions: string;
  referenceCode: string;
  requiredCount: number;
  exampleInput?: unknown;
}): GeneratedSuite {
  const { challengeTitle, language, directions, referenceCode, requiredCount, exampleInput } = payload;

  const constraintHints = extractConstraintHints(directions);
  const seed = fnv1a32(`${challengeTitle}::${directions}`);
  const rng = mulberry32(seed);

  const inferredFromExample = exampleInput ? inferModelFromExample(exampleInput) : null;
  const model = inferredFromExample || inferInputModel(directions, referenceCode);

  let plannedCases: Array<{ category: string; input: unknown; expected?: unknown; rationale: string }> = [];

  if (exampleInput && typeof exampleInput === "object" && !Array.isArray(exampleInput)) {
    plannedCases = buildMutatedObjectCases({
      baseInput: exampleInput as Record<string, unknown>,
      constraints: constraintHints,
      requiredCount,
      rng,
    });
  } else {
    const curatedCases = casesByModel(model, constraintHints);
    const fuzzCases = buildFuzzTuplesForModel({
      model,
      constraints: constraintHints,
      rng,
      targetCount: Math.max(0, requiredCount * 4),
    });

    plannedCases = expandToCount({
      curatedCases,
      fuzzCases,
      requiredCount,
    });
  }

  if (plannedCases.length < requiredCount) {
    const seenInputs = new Set(plannedCases.map((entry) => stableKey(entry.input)));

    // If the first pass dedupes too aggressively, keep generating until we hit the exact target.
    for (let attempt = 0; attempt < 4 && plannedCases.length < requiredCount; attempt += 1) {
      if (exampleInput && typeof exampleInput === "object" && !Array.isArray(exampleInput)) {
        const supplementalCases = buildMutatedObjectCases({
          baseInput: exampleInput as Record<string, unknown>,
          constraints: constraintHints,
          requiredCount: Math.max(requiredCount * 2, requiredCount + 32),
          rng,
        });

        for (const entry of supplementalCases) {
          if (plannedCases.length >= requiredCount) break;
          const key = stableKey(entry.input);
          if (seenInputs.has(key)) continue;
          seenInputs.add(key);
          plannedCases.push(entry);
        }
      } else {
        const supplementalTuples = buildFuzzTuplesForModel({
          model,
          constraints: constraintHints,
          rng,
          targetCount: Math.max(requiredCount * 4, requiredCount + 64),
        });

        for (const tuple of supplementalTuples) {
          if (plannedCases.length >= requiredCount) break;
          const entry = tupleToPlannedCase(tuple);
          const key = stableKey(entry.input);
          if (seenInputs.has(key)) continue;
          seenInputs.add(key);
          plannedCases.push(entry);
        }
      }
    }

    let fallbackIndex = plannedCases.length;
    while (plannedCases.length < requiredCount) {
      const fallbackCase = buildFallbackPlannedCase({
        model,
        exampleInput,
        index: fallbackIndex,
      });
      fallbackIndex += 1;

      const key = stableKey(fallbackCase.input);
      if (seenInputs.has(key)) continue;
      seenInputs.add(key);
      plannedCases.push(fallbackCase);
    }
  }

  plannedCases = plannedCases.slice(0, requiredCount);

  const testCases = plannedCases.map((entry, idx) =>
    buildCase(
      `TC-${String(idx + 1).padStart(4, "0")}`,
      entry.category,
      entry.input,
      entry.expected ?? "depends-on-spec",
      entry.rationale
    )
  );

  return {
    metadata: {
      challengeTitle,
      language,
      inferredInputModel: model,
      requiredCount,
      generatedCount: testCases.length,
      constraintHints,
      seed,
      generatedAt: new Date().toISOString(),
      note:
        "Expected outputs are placeholders unless you enable reference execution. Always validate final answers with a trusted solution before grading.",
    },
    testCases,
  };
}
